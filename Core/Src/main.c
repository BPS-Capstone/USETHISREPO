//Redirect scanf to point to the input buffer
int copyReadBuffer(uint8_t * buf){
	memset(buf, '\0', sizeof(buf));

	if (rx_buffer[0] == '\0') return -1;


	int count = 0;
	for (int i = 0; i < sizeof(rx_buffer); i++){
		buf[i] = rx_buffer[i];
		count++;
		if (buf[i] == '\0') break;
	}

	buf[63] = '\0';
	memset(rx_buffer, '\0', sizeof(rx_buffer));
	bufCount = 0;
	return count;

}


